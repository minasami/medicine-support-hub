-- Search candidates against indexed base columns first, then join the enriched
-- view only for the small result set. This avoids scanning the full joined view.
create or replace function public.search_medicines_catalog(
  p_query text,
  p_limit integer default 50
)
returns setof public.medicines_catalog_enriched_v1
language sql
stable
security invoker
set search_path = public
as $$
  with input as (
    select trim(coalesce(p_query, '')) as query,
           greatest(1, least(coalesce(p_limit, 50), 100)) as row_limit
  ), candidates as (
    select m.id, 2000::numeric as score
    from public.medicines2 m, input i
    where i.query <> ''
      and coalesce(m.active, true) = true
      and (m.barcode = i.query or m.code = i.query or m.custom_product_code = i.query)

    union all

    select m.id, (1000 + similarity(m.name_en, i.query) * 100)::numeric as score
    from public.medicines2 m, input i
    where length(i.query) >= 2
      and coalesce(m.active, true) = true
      and m.name_en is not null
      and (m.name_en % i.query or m.name_en ilike '%' || i.query || '%')

    union all

    select m.id, (1000 + similarity(m.name_ar, i.query) * 100)::numeric as score
    from public.medicines2 m, input i
    where length(i.query) >= 2
      and coalesce(m.active, true) = true
      and m.name_ar is not null
      and (m.name_ar % i.query or m.name_ar ilike '%' || i.query || '%')

    union all

    select m.id, 700::numeric as score
    from public.medicines2 m, input i
    where length(i.query) >= 2
      and coalesce(m.active, true) = true
      and (m.barcode ilike '%' || i.query || '%' or m.code ilike '%' || i.query || '%')

    union all

    select m.id, 1::numeric as score
    from public.medicines2 m, input i
    where i.query = '' and coalesce(m.active, true) = true
  ), ranked as (
    select c.id, max(c.score) as score
    from candidates c
    group by c.id
    order by max(c.score) desc, c.id
    limit (select row_limit from input)
  )
  select enriched.*
  from ranked
  join public.medicines_catalog_enriched_v1 enriched on enriched.id = ranked.id
  order by ranked.score desc, coalesce(enriched.name_en, enriched.name_ar), enriched.id;
$$;

