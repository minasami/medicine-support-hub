  catalog.*,
  coalesce(catalog.category, egyptdwa.category_name) as display_category,
  egyptdwa.category_name as egyptdwa_category,
  egyptdwa.category_url as egyptdwa_category_url,
  egyptdwa.image_url as egyptdwa_image_url,
  egyptdwa.source_url as egyptdwa_source_url,
