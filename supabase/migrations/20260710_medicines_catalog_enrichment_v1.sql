grant execute on function public.search_medicines_catalog(text, integer) to anon, authenticated;

create or replace function public.search_medicines_catalog_index(
  p_query text,
  p_limit integer default 60
)
returns table (
  entity_type text,
  entity_key text,
  title text,
  subtitle text,
  href text,
  category text,
  weight numeric
)
language sql
stable
security invoker
set search_path = public
as $$
  select
    'catalog_product'::text,
    product.id::text,
    coalesce(nullif(product.name_en, ''), nullif(product.name_ar, ''), 'Product #' || product.id::text),
    concat_ws(
      ' · ',
      nullif(product.name_ar, ''),
      product.barcode,
      product.code,
      case when product.price is not null then product.price::text || ' EGP' end,
      product.display_category
    ),
    '/catalog/' || product.id::text,
    coalesce(product.display_category, 'medicine catalog'),
    (case when product.barcode is not null then 30 else 20 end
      + case when product.enrichment_source_count > 0 then 5 else 0 end)::numeric
  from public.search_medicines_catalog(p_query, p_limit) product;
$$;

