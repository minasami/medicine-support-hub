with source_parts as (
  select
    m.med_name,
    m.med_url,
    trim(regexp_replace(part, '\s+[0-9].*$', '', 'i')) as ingredient_name,
    regexp_replace(coalesce(m.disease_name, ''), '\s*\([0-9,]+\)\s*$', '', 'g') as disease_area,
    m.prescription_required,
    nullif(trim(regexp_replace(coalesce(m.drug_manufacturer, ''), '^\s*\*?\s*Mkt:\s*', '', 'i')), '') as manufacturer_name,
    nullif(trim(regexp_replace(coalesce(m.drug_manufacturer_origin, ''), '^\s*\*?\s*Country of Origin:\s*', '', 'i')), '') as manufacturer_origin
  from public.medicines4 m
  cross join lateral regexp_split_to_table(
    coalesce(public.clean_netmeds_generic(m.generic_name), ''),
    '\+'
  ) part
), cleaned as (
  select
    public.normalize_ascii_catalog_name(ingredient_name) as ingredient_key,
    ingredient_name,
    med_name,
    med_url,
    nullif(disease_area, '') as disease_area,
    prescription_required,
    manufacturer_name,
    manufacturer_origin
  from source_parts
  where length(trim(ingredient_name)) >= 3
    and public.normalize_ascii_catalog_name(ingredient_name) <> ''
), aggregated as (
  select
    ingredient_key,
    min(ingredient_name) as ingredient_name,
    count(distinct med_name)::bigint as source_product_count,
    coalesce(array_agg(distinct disease_area) filter (where disease_area is not null), '{}') as disease_areas,
    bool_or(lower(coalesce(prescription_required, '')) like '%rx%') as prescription_required_observed,
    min(manufacturer_name) filter (where manufacturer_name is not null) as manufacturer_sample,
    min(manufacturer_origin) filter (where manufacturer_origin is not null) as manufacturer_origin_sample,
    min(med_url) filter (where med_url is not null) as source_url_sample
  from cleaned
  group by ingredient_key
)
insert into public.international_ingredient_reference (
  ingredient_key, ingredient_name, source_product_count, disease_areas,
  prescription_required_observed, manufacturer_sample,
  manufacturer_origin_sample, source_url_sample, updated_at
)
select
  ingredient_key, ingredient_name, source_product_count, disease_areas,
  prescription_required_observed, manufacturer_sample,
  manufacturer_origin_sample, source_url_sample, now()
from aggregated
on conflict (ingredient_key) do update set
  ingredient_name = excluded.ingredient_name,
  source_product_count = excluded.source_product_count,
  disease_areas = excluded.disease_areas,
  prescription_required_observed = excluded.prescription_required_observed,
  manufacturer_sample = excluded.manufacturer_sample,
  manufacturer_origin_sample = excluded.manufacturer_origin_sample,
  source_url_sample = excluded.source_url_sample,
  updated_at = now();

