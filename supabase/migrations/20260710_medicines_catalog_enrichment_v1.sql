  egyptdwa.category_views as egyptdwa_category_views,
  egyptdwa.observed_price as egyptdwa_observed_price,
  egyptdwa.observed_currency as egyptdwa_observed_currency,
  egyptdwa.match_method as egyptdwa_match_method,
  egyptdwa.match_score as egyptdwa_match_score,
  netmeds.generic_name as international_generic_name,
  netmeds.disease_area as international_disease_area,
  netmeds.prescription_required as international_prescription_signal,
  netmeds.manufacturer_name as international_manufacturer,
  netmeds.manufacturer_origin as international_manufacturer_origin,
  netmeds.source_url as international_source_url,
  netmeds.image_url as international_image_url,
  netmeds.observed_price_text as international_observed_price_text,
  netmeds.observed_currency as international_observed_currency,
  netmeds.match_method as international_match_method,
  netmeds.match_score as international_match_score,
  ((egyptdwa.id is not null)::integer + (netmeds.id is not null)::integer) as enrichment_source_count
