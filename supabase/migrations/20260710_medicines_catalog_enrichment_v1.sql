-- Source-backed enrichment for the medicines2 catalog.
-- Core rules:
--   * medicines2 IDs, Egyptian prices, barcodes, codes, and inventory boundaries remain authoritative.
--   * medicines3 (EgyptDwa) may contribute Egyptian category, image, popularity, source URL, and observed-price evidence.
--   * medicines4 (Netmeds) is an international reference. INR prices never replace Egyptian catalog prices.
--   * Only verified product-level matches are exposed through the enriched catalog.
--   * Raw source tables remain private and their long-form content is never exposed.

create extension if not exists pg_trgm;

create or replace function public.normalize_catalog_name(value text)
returns text
language sql
immutable
parallel safe
set search_path = public
as $$
  select regexp_replace(
    lower(translate(coalesce(value, ''), 'أإآٱةىؤئًٌٍَُِّْـ', 'ااااهيوي')),
    '[^a-z0-9ء-ي]+',
    '',
    'g'
  );
$$;

create or replace function public.normalize_ascii_catalog_name(value text)
returns text
language sql
immutable
parallel safe
set search_path = public
as $$
  select regexp_replace(lower(coalesce(value, '')), '[^a-z0-9]+', '', 'g');
$$;

create or replace function public.catalog_number_signature(value text)
returns text
language sql
immutable
parallel safe
set search_path = public
as $$
  select regexp_replace(coalesce(value, ''), '[^0-9]+', '', 'g');
$$;

create or replace function public.canonical_source_url(value text)
returns text
language sql
immutable
parallel safe
set search_path = public
as $$
  select nullif(split_part(trim(coalesce(value, '')), ' ', 1), '');
$$;

create or replace function public.clean_netmeds_generic(value text)
returns text
language sql
immutable
parallel safe
set search_path = public
as $$
  select nullif(trim(
    regexp_replace(
      regexp_replace(coalesce(value, ''), '^\s*Generic Name\s*', '', 'i'),
      '\s+Also Known As:.*$',
      '',
      'i'
    )
  ), '');
$$;

