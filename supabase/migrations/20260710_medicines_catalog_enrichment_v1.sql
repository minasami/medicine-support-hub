create or replace view public.medicine_catalog_enrichment_metrics
with (security_invoker = true)
as
select
  count(*) filter (where source_system = 'egyptdwa' and confidence = 'verified')::bigint as verified_egyptdwa_products,
  count(*) filter (where source_system = 'netmeds' and confidence = 'verified')::bigint as verified_netmeds_products,
  count(*) filter (where confidence = 'review_required')::bigint as review_required_products,
  (select count(*) from public.international_ingredient_reference)::bigint as international_ingredient_references,
  max(updated_at) as latest_evidence_update
from public.medicine_catalog_source_evidence;

grant select on public.medicine_catalog_enrichment_metrics to anon, authenticated;
