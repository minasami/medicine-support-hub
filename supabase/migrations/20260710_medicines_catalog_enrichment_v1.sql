-- EgyptDwa tier 1: normalized full name is unique in both datasets.
with source_rows as (
  select
    "Medicine Name" as source_key,
    public.normalize_catalog_name("Medicine Name") as norm,
    "Price"::numeric as source_price,
    "Category title" as category_name,
    "Category link" as category_url,
    "Views" as product_views,
    "Category views" as category_views,
    "Image" as image_url,
    public.canonical_source_url("Medicine Name link") as source_url
  from public.medicines3
), source_unique as (
  select norm, min(source_key) as source_key
  from source_rows
  where length(norm) >= 5 and norm ~ '[a-zء-ي]'
  group by norm
  having count(*) = 1
), target_rows as (
  select id, public.normalize_catalog_name(name_ar) as norm
  from public.medicines2
  where coalesce(active, true) = true
), target_unique as (
  select norm, min(id) as medicines2_id
  from target_rows
  where length(norm) >= 5 and norm ~ '[a-zء-ي]'
  group by norm
  having count(*) = 1
)
insert into public.medicine_catalog_source_evidence (
  source_system, source_key, medicines2_id, match_method, confidence, match_score,
  source_name, source_url, image_url, category_name, category_url,
  product_views, category_views, observed_price, observed_price_text, observed_currency,
  evidence
)
select
  'egyptdwa', s.source_key, t.medicines2_id,
  'unicode_normalized_name_unique', 'verified', 1.0,
  s.source_key, s.source_url, s.image_url, s.category_name, s.category_url,
  s.product_views, s.category_views, s.source_price, s.source_price::text, 'EGP',
  jsonb_build_object('name_normalization', 'unicode_preserving')
from source_rows s
join source_unique su on su.norm = s.norm and su.source_key = s.source_key
join target_unique t using (norm)
on conflict (source_system, source_key) do nothing;

-- EgyptDwa tier 2: exact normalized name has multiple catalog candidates,
-- but exactly one candidate has the same Egyptian observed price.
with source_rows as (
  select
    "Medicine Name" as source_key,
    public.normalize_catalog_name("Medicine Name") as norm,
    "Price"::numeric as source_price,
    "Category title" as category_name,
    "Category link" as category_url,
    "Views" as product_views,
    "Category views" as category_views,
    "Image" as image_url,
    public.canonical_source_url("Medicine Name link") as source_url
  from public.medicines3
), candidates as (
  select
    s.*,
    m.id as medicines2_id,
    count(*) filter (
      where s.source_price is not null
        and m.price is not null
        and abs(s.source_price - m.price) <= 0.01
    ) over (partition by s.source_key) as matching_price_candidates
  from source_rows s
  join public.medicines2 m
    on public.normalize_catalog_name(m.name_ar) = s.norm
   and coalesce(m.active, true) = true
  where length(s.norm) >= 5 and s.norm ~ '[a-zء-ي]'
)
insert into public.medicine_catalog_source_evidence (
  source_system, source_key, medicines2_id, match_method, confidence, match_score,
  source_name, source_url, image_url, category_name, category_url,
  product_views, category_views, observed_price, observed_price_text, observed_currency,
  evidence
)
select
  'egyptdwa', c.source_key, c.medicines2_id,
  'unicode_normalized_name_exact_price_unique', 'verified', 0.99,
  c.source_key, c.source_url, c.image_url, c.category_name, c.category_url,
  c.product_views, c.category_views, c.source_price, c.source_price::text, 'EGP',
  jsonb_build_object('price_tolerance', 0.01)
from candidates c
where c.matching_price_candidates = 1
  and c.source_price is not null
  and exists (
    select 1 from public.medicines2 m
    where m.id = c.medicines2_id
      and m.price is not null
      and abs(c.source_price - m.price) <= 0.01
  )
on conflict do nothing;

-- EgyptDwa tier 3: conservative spelling variation. A candidate must have:
--   * the same price,
--   * the same complete numeric signature (strength/pack numbers),
--   * trigram similarity >= 0.90,
--   * at least 0.08 separation from the second-best candidate.
with source_rows as (
  select
    "Medicine Name" as source_key,
    "Price"::numeric as source_price,
    public.catalog_number_signature("Medicine Name") as number_signature,
    "Category title" as category_name,
    "Category link" as category_url,
    "Views" as product_views,
    "Category views" as category_views,
    "Image" as image_url,
    public.canonical_source_url("Medicine Name link") as source_url
  from public.medicines3
  where "Medicine Name" is not null
), ranked as (
  select
    s.*,
    candidate.id as medicines2_id,
    candidate.similarity_score,
    row_number() over (
      partition by s.source_key
      order by candidate.similarity_score desc, candidate.id
    ) as candidate_rank
  from source_rows s
  cross join lateral (
    select
      m.id,
      similarity(m.name_ar, s.source_key) as similarity_score
    from public.medicines2 m
    where coalesce(m.active, true) = true
      and s.number_signature <> ''
      and public.catalog_number_signature(m.name_ar) = s.number_signature
      and s.source_price is not null
      and m.price is not null
      and abs(s.source_price - m.price) <= 0.01
      and m.name_ar % s.source_key
    order by similarity(m.name_ar, s.source_key) desc, m.id
    limit 2
  ) candidate
), compared as (
  select
    ranked.*,
    max(similarity_score) filter (where candidate_rank = 2)
      over (partition by source_key) as second_similarity
  from ranked
)
insert into public.medicine_catalog_source_evidence (
  source_system, source_key, medicines2_id, match_method, confidence, match_score,
  source_name, source_url, image_url, category_name, category_url,
  product_views, category_views, observed_price, observed_price_text, observed_currency,
  evidence
)
select
  'egyptdwa', c.source_key, c.medicines2_id,
  'arabic_trigram_price_numbers', 'verified', c.similarity_score,
  c.source_key, c.source_url, c.image_url, c.category_name, c.category_url,
  c.product_views, c.category_views, c.source_price, c.source_price::text, 'EGP',
  jsonb_build_object(
    'number_signature', c.number_signature,
    'second_similarity', coalesce(c.second_similarity, 0),
    'minimum_similarity', 0.90,
    'minimum_margin', 0.08
  )
from compared c
where c.candidate_rank = 1
  and c.similarity_score >= 0.90
  and c.similarity_score - coalesce(c.second_similarity, 0) >= 0.08
on conflict do nothing;

-- Netmeds product-level evidence is accepted only for unique normalized English
-- names on both sides. Foreign price strings are retained as evidence only.
with source_rows as (
  select
    med_name as source_key,
    public.normalize_ascii_catalog_name(med_name) as norm,
    med_url as source_url,
    public.canonical_source_url(img_urls) as image_url,
    public.clean_netmeds_generic(generic_name) as generic_name,
    regexp_replace(coalesce(disease_name, ''), '\s*\([0-9,]+\)\s*$', '', 'g') as disease_area,
    prescription_required,
    nullif(trim(regexp_replace(coalesce(drug_manufacturer, ''), '^\s*\*?\s*Mkt:\s*', '', 'i')), '') as manufacturer_name,
    nullif(trim(regexp_replace(coalesce(drug_manufacturer_origin, ''), '^\s*\*?\s*Country of Origin:\s*', '', 'i')), '') as manufacturer_origin,
    final_price as observed_price_text
  from public.medicines4
), source_unique as (
  select norm, min(source_key) as source_key
  from source_rows
  where length(norm) >= 6 and norm ~ '[a-z]'
  group by norm
  having count(*) = 1
), target_rows as (
  select id, public.normalize_ascii_catalog_name(name_en) as norm
  from public.medicines2
  where coalesce(active, true) = true
), target_unique as (
  select norm, min(id) as medicines2_id
  from target_rows
  where length(norm) >= 6 and norm ~ '[a-z]'
  group by norm
  having count(*) = 1
)
insert into public.medicine_catalog_source_evidence (
  source_system, source_key, medicines2_id, match_method, confidence, match_score,
  source_name, source_url, image_url, observed_price_text, observed_currency,
  generic_name, disease_area, prescription_required, manufacturer_name,
  manufacturer_origin, evidence
)
select
  'netmeds', s.source_key, t.medicines2_id,
  'ascii_normalized_name_unique', 'verified', 1.0,
  s.source_key, s.source_url, s.image_url, s.observed_price_text, 'INR',
  s.generic_name, nullif(s.disease_area, ''), s.prescription_required,
  s.manufacturer_name, s.manufacturer_origin,
  jsonb_build_object(
    'market', 'India',
    'price_policy', 'reference_only_never_overwrite_egyptian_price'
  )
from source_rows s
join source_unique su on su.norm = s.norm and su.source_key = s.source_key
join target_unique t using (norm)
on conflict do nothing;

-- Standalone international ingredient reference. This connects the platform to
-- medicines4 knowledge without claiming that Indian brands equal Egyptian brands.
create table if not exists public.international_ingredient_reference (
  ingredient_key text primary key,
  ingredient_name text not null,
  source_system text not null default 'netmeds' check (source_system = 'netmeds'),
  source_product_count bigint not null,
  disease_areas text[] not null default '{}',
  prescription_required_observed boolean not null default false,
  manufacturer_sample text,
  manufacturer_origin_sample text,
  source_url_sample text,
  updated_at timestamptz not null default now()
);

